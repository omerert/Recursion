func factorial(x: Int) -> Int{
    print("Calculating factorial(\(x))...")
    if x <= 1{
        print("Terminal case, returning 1.")
        return 1
    }
    else{
        let y =  x * factorial(x: x-1) 
        print(" Non-terminal case, returning \(y).")
        return y
    }
}
print("The final result is: \(factorial(x: n)). ")
