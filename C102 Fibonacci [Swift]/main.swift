//fibonacci function
func fibo(x: Int) -> Int{
    print("Calculating fibonacci(\(x))...")
    //if fibonacci value is less than or equal to 2
    if (x <= 2){
        print("Terminal case, returning 1.")
        return 1
    }
    //if fibonacci value is not less than or equal to 2
    else{
        //fibonacci formula
        let y = fibo(x: x-1) + fibo(x: x-2) 
        print("Non-terminal case, returning \(y).")
        return y
    }
    
}
